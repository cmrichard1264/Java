package day22_NestedLoop;

public class ForLoopStars {
public static void main(String[] args) {
	
	
	for(int a=1; a<=10; a++) {
	for(int i =1; i<=a; i++) {
		System.out.print("@");
	}
	
	System.out.println();
	
	}
	
	//example2
	
	for (int i=10; i>=1; i--) {
        for (int j=1; j<=i; j++) {
            System.out.print("@");
        }
    System.out.println();
    }

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
