package home_work;

public class Task05_number {

	public static void main(String[] args) {

		int num = -500+300;

		if (num > 0) {
			System.out.println("positive");
		}

		if (num < 0) {
			System.out.println("negative");
		}

		if (num == 0) {
			System.out.println("zero");
		}

	}

}
